def rectanglePerimeter(l, w):
    # Citation - https://www.smartick.com/blog/mathematics/geometry/how-to-calculate-perimeters-1/
    # Citation - Author: Smartick
    # Citation - Date Written - 6-3-2019
    # Citation - Date Accessed: 9-11-25
    return 2 * (l + w)

def areaOfCircle(r):
    # Citation - https://www.khanacademy.org/math/8th-grade-foundations-engageny/8th-m5-engage-ny-foundations/8th-m5-tb-foundations/a/area-of-a-circle
    # Citation - Author: Khan Academy
    # Citation - Date Published: 12-23-2019
    # Citation - Date Accessed: 9-11-25
    return math.pi * (r ** 2)

def circleCircumference(r):
    # Citation - https://www.khanacademy.org/a/radius-diameter-circumference
    # Citation - Author: Khan Academy
    # Citation - Date Published:2-12-2020 
    # Citation - Date Accessed: 9-11-25
    return 2 * math.pi * r